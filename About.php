                            <h2>Creating programs that are designed to empower</h2>
<p>The Canadian Mental Health Association (CMHA), founded in 1918, is one of the oldest voluntary organizations in Canada. Each year, we provide direct service to more than 100,000 Canadians through the combined efforts of more than 10,000 volunteers and staff across Canada in over 120 communities.</p>
<p>As a nation-wide, voluntary organization, the Canadian Mental Health Association promotes the mental health of all and supports the resilience and recovery of people experiencing mental illness. The CMHA accomplishes this mission through advocacy, education, research and service.</p>
<p>Local CMHA offices across Canada provide a wide range of innovative services and supports to people who are experiencing mental illness and their families. These services are tailored to the needs and resources of the communities where they are based. One of the core goals of these services is to help people with mental illness develop the personal tools to lead meaningful and productive lives. CMHA Edmonton has been providing services in the Edmonton region since 1954.</p>
