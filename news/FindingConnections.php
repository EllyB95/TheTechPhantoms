                        <p>Exploring thoughts, ideas and sentiments on how to share a social responsibility while keeping open hearts and open minds to nurture ongoing and new connections and a sense of belonging.</p>
<p>During a heartwarming and insightful conversation with award-winning independent documentary filmmaker and journalist, Brandy Yanchyk, we discuss the topics of connection and belonging, how our self-image influences our actions, and how we can find ideas and common grounds to help us grow closer.</p>
